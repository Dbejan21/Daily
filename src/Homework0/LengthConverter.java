package Homework0;

public class LengthConverter
{
    public static void main(String[] args)
    {
        double meter, foot, centimeter;
        meter = 6;
        foot = meter * 3.281;

        System.out.println("How many foot in 6 meters ? ");
        System.out.println(foot);

        centimeter = meter * 100;

        System.out.println("How many centimeters in 6 meters ? ");
        System.out.println(centimeter);
    }
}
